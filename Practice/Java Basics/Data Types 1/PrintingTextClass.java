package printingtextpackage;

public class PrintingTextClass 
{

	public static void main(String[] args) 
	{
		// this program is for practicing different Java 
		// data types and printing them
		
		// make some random variables of different data types
		String itemName = "Candy Bar";
		int numberOfItems = 3;
		float costOfItem = 2.99f;
		float totalCost = costOfItem * numberOfItems;
		char currency = '$';
		
		// print the variables 
		System.out.println("Item: " + itemName);
		System.out.println("Number of Items: " + numberOfItems);
		System.out.println("Cost per Item: " + costOfItem);
		System.out.println("Total cost: " + currency + totalCost);
	
	}

}
